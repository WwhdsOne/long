const props = defineProps({
  pattern: {
    type: String,
    default: 'shield',
  },
  paletteName: {
    type: String,
    default: '',
  },
})

const canvasRef = ref(null)
let raf = 0
let particles = []
let phase = 'idle'
let phaseTimer = 0

function resolvePalette() {
  const name = props.paletteName || PATTERN_DEFAULT_PALETTE[props.pattern] || 'steel'
  return PALETTES[name] || PALETTES.steel
}

function resolvePattern() {
  return PATTERNS[props.pattern] || SHIELD_PATTERN
}

function createParticles() {
  const list = []
  const pattern = resolvePattern()
  for (let y = 0; y < GRID; y++) {
    for (let x = 0; x < GRID; x++) {
      const ci = pattern[y][x]
      if (ci === 0) continue
      list.push({
        x: x * PX + PX / 2,
        y: y * PX + PX / 2,
        vx: 0,
        vy: 0,
        opacity: 1,
        colorIndex: ci,
      })
    }
  }
  return list
}

function explode() {
  const W = GRID * PX
  const H = GRID * PX
  const cx = W / 2
  const cy = H / 2
  for (const p of particles) {
    const dx = p.x - cx
    const dy = p.y - cy
    const dist = Math.sqrt(dx * dx + dy * dy) || 1
    const speed = 1.2 + Math.random() * 2.8
    p.vx = (dx / dist) * speed + (Math.random() - 0.5) * 1.5
    p.vy = (dy / dist) * speed + (Math.random() - 0.5) * 1.5
  }
}

function draw(ctx) {
  const palette = resolvePalette()
  ctx.clearRect(0, 0, GRID * PX, GRID * PX)
  for (const p of particles) {
    if (p.opacity <= 0) continue
    ctx.globalAlpha = p.opacity
    ctx.fillStyle = palette[p.colorIndex] || palette[2]
    ctx.fillRect(Math.round(p.x - PX / 2), Math.round(p.y - PX / 2), PX, PX)
  }
  ctx.globalAlpha = 1
}

function frame() {
  const canvas = canvasRef.value
  if (!canvas) return
  const ctx = canvas.getContext('2d')
  const W = GRID * PX
  const H = GRID * PX

  phaseTimer += 16

  if (phase === 'idle' && phaseTimer > 500) {
    phase = 'explode'
    phaseTimer = 0
    explode()
  }

  if (phase === 'explode') {
    let alive = 0
    for (const p of particles) {
      if (p.opacity <= 0) continue
      p.x += p.vx
      p.y += p.vy
      p.vx *= 0.985
      p.vy *= 0.985
      if (p.x < -6 || p.x > W + 6 || p.y < -6 || p.y > H + 6) {
        p.opacity = Math.max(0, p.opacity - 0.08)
      } else if (phaseTimer > 300) {
        p.opacity = Math.max(0, p.opacity - 0.015)
      }
      if (p.opacity > 0) alive++
    }
    if (alive === 0) {
      phase = 'done'
    }
  }

  draw(ctx)

  if (phase !== 'done') {
    raf = requestAnimationFrame(frame)
  }
}

onMounted(() => {
  particles = createParticles()
  const canvas = canvasRef.value
  if (canvas) {
    draw(canvas.getContext('2d'))
  }
  raf = requestAnimationFrame(frame)
})

onBeforeUnmount(() => {
  cancelAnimationFrame(raf)
})
